/**
 * Returns an inline SVG string for the given icon name.
 * @param name - Icon identifier from the IconName type
 * @param size - Optional size in pixels (default: 16)
 * @param className - Optional CSS class name
 */
export function icon(name: IconName, size = 16, className = ""): string {
  const paths = svgPaths[name];
  if (!paths) return "";
  const cls = className ? ` class="${className}"` : "";
  return `<svg${cls} xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true">${paths}</svg>`;
}

/**
 * Returns an icon wrapped in a <span> with aria-hidden for use inside buttons.
 */
export function btnIcon(name: IconName, size = 15): string {
  return icon(name, size);
}
